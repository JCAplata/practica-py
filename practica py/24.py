# Creamos una lista de nombres de lenguajes de programación
lista = ['Python', 'C', 'JavaScript', 'Go', 'Java']
# Imprimimos la lista original
print(lista)
# Ordenamos la lista por la longitud de cada elemento
lista.sort(key=len)
# Imprimimos la lista ordenada por longitud
print(lista)
# Ordenamos la lista en orden alfabético
lista.sort()
# Imprimimos la lista ordenada alfabéticamente
print(lista)
